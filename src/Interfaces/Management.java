    public void setDetails() {
        HallResourcesHandler handler = new HallResourcesHandler();
        ArrayList<Hall> hall;


        try {
            hall = handler.viewHalls();
            allocateHall.removeAllItems();
            for (int i = 0; i < hall.size(); i++) {

                allocateHall.addItem(hall.get(i).getHallID());
            }
        } catch (SQLException ex) {
            Logger.getLogger(Management.class.getName()).log(Level.SEVERE, null, ex);
        }
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
        Date date1 = new Date();
        allocateDate.setText(dateFormat.format(date1).toString());
        ArrayList<Employee> arr;
        EmployeeDataHandler handle = new EmployeeDataHandler();
        arr = handle.viewEmployeeset();
        if (arr.size() > 0) {
            allocateEmp.removeAllItems();
            for (int i = 0; i < arr.size(); i++) {
                allocateEmp.addItem(arr.get(i).getEmployID());
            }
        }
    }

