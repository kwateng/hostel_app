    private void searchStudentTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchStudentTextFieldKeyReleased
        ArrayList<Student> temp = new ArrayList<Student>();
        if (searchStudentTextField.getText().equals("")) {
            temp = allStudents;
        } else {
            for (int i = 0; i < allStudents.size(); i++) {
                if (allStudents.get(i).getIndexNo().startsWith(searchStudentTextField.getText())) {
                    temp.add(allStudents.get(i));
                }
            }
        }

        updateStudentTable(temp);
    }//GEN-LAST:event_searchStudentTextFieldKeyReleased

