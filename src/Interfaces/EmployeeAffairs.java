    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        ArrayList<Employee_worksIn> employList = new ArrayList<Employee_worksIn>();
        int count = 0;
        EmployeeDataHandler dh = new EmployeeDataHandler();

        
          String date =StaticFields.getDate();

        if (!idField.getText().equals("")) {
            employList = dh.EmployRoomDetails(idField.getText());
        } else {
            idField.requestFocus();
        }
        if (employList != null) {

            employHallTable.removeAll();
            DefaultTableModel mode = new DefaultTableModel();
            employHallTable.setModel(mode);
            String headers[] = new String[3];
            headers[0] = "Employ ID";
            headers[1] = "Hall ID";
            headers[2] = "Date";
            mode.setColumnIdentifiers(headers);
            //System.out.println(eHList.size());

            TodayTimeTable.removeAll();
            DefaultTableModel model = new DefaultTableModel();
            TodayTimeTable.setModel(model);
            String headers1[] = new String[3];
            headers1[0] = "Employ ID";
            headers1[1] = "Hall ID";
            headers1[2] = "Date";
            model.setColumnIdentifiers(headers1);

            for (int i = 0; i < employList.size(); i++) {

                if (!employList.get(i).getDate().equals(date)) {
                    mode.insertRow(i, new Object[]{employList.get(i).getEmployId(), employList.get(i).getHallId(), employList.get(i).getDate()});
                } else {
                    count++;
                    model.insertRow(count - 1, new Object[]{employList.get(i).getEmployId(), employList.get(i).getHallId(), employList.get(i).getDate()});
                }
            }


        }
}//GEN-LAST:event_jButton20ActionPerformed

