    private void uploadImageButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadImageButtonActionPerformed
        String filename;
        int w, h;
        JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(this);
        filename = fc.getSelectedFile().getAbsolutePath();

        try {
            BufferedImage bImage = ImageIO.read(new File(filename));
            w = bImage.getWidth(null);
            h = bImage.getHeight(null);
            if (bImage.getType() != BufferedImage.TYPE_INT_RGB) {
                BufferedImage bi2 =
                        new BufferedImage(w, h, BufferedImage.TYPE_INT_RGB);
                Graphics big = bi2.getGraphics();
                big.drawImage(bImage, 0, 0, null);
                icon = new ImageIcon(filename);
                pictureUploadLable.setIcon(icon);
                //Create a label with your image file
                JLabel label = new JLabel(icon);
                //Create a window using JFrame with title ( Put image on JLabel )
                JFrame frame = new JFrame("Put image on JLabel");
                //Add created label into JFrame
                frame.add(label);
                //Set default close operation for JFrame
                frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                //Set JFrame size
                frame.setSize(400, 400);
                //Make JFrame visible. So we can see it.
                frame.setLocationRelativeTo(null);
                frame.setVisible(true);
            }
        } catch (IOException e) {
            System.out.println("Image could not be read");
        }
}//GEN-LAST:event_uploadImageButtonActionPerformed

